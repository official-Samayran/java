package methods;

public class example1 {

    //Defining new method Named "samayran"
    public static void samayran() {
        System.out.println("My name is Samayran");
    }

    //Defining new method named "java"
    public static void java() {
        System.out.println("This code is written in JAVA language.");
    }

    //calling both methods in main method
    public static void main (String[] args) {
        samayran();
        java();
    }
}
