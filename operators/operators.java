package operators;
public class operators {
    public static void main(String[] args){

        // + - * / %
        int a = 10, b = 5;
        
        System.out.println("A = 10 | B = 5");
        System.out.println("A + B = " + (a+b));
        System.out.println("A - B = " + (a-b));
        System.out.println("A * B = " + (a*b));
        System.out.println("A / B = " + (a/b));
        System.out.println("A % B = " + (a%b));
        
        

    }    
}
